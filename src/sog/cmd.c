#include <string.h>

#include "typedef.h"
#include "cmd.h"
#include "str.h"

extern CMD_DATA cmd_table[]; /* temporary fix before varr cmd impl */

CMD_DATA *cmd_lookup(const char *name)
{
	CMD_DATA *cmd;

	for (cmd = cmd_table; cmd->name; cmd++)
		if (!str_cmp(cmd->name, name))
			return cmd;

	return NULL;
}

